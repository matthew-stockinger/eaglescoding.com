function w3_open() {
    document.getElementById("mySidebar").style.display = "block";
    document.getElementById("mySidebar").style.height = "400px";
    document.getElementById("navaning").style.display = "none";
}

function w3_close() {
    document.getElementById("mySidebar").style.display = "none";
    document.getElementById("mySidebar").style.height = "69px";
    document.getElementById("navaning").style.display = "block";
}